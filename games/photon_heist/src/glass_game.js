document.addEventListener("keydown", e => {
  if (!glassEl.stageModal.hidden) {
    if (e.key === "Escape") closeGlassModal();
    return;
  }
  if (e.key === "ArrowLeft"  || e.key === ",") rotateBy(-1, e.shiftKey && glassState.level.fineStepDeg ? glassState.level.fineStepDeg : null);
  if (e.key === "ArrowRight" || e.key === ".") rotateBy(+1, e.shiftKey && glassState.level.fineStepDeg ? glassState.level.fineStepDeg : null);
  if ((e.key === "Enter" || e.key === " ") && glassState.level.commitMode) {
    e.preventDefault();
    fireGlassLaser();
  }
});

