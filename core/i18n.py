def normalize_lang(value: object) -> str:
    if not isinstance(value, str):
        return DEFAULT_LANG
    return _LANG_ALIASES.get(value.strip().lower(), DEFAULT_LANG)


def get_lang() -> str:
    lang = normalize_lang(st.session_state.get("lang", DEFAULT_LANG))
    st.session_state["lang"] = lang
    return lang


def set_lang(lang_code: str) -> None:
    st.session_state["lang"] = normalize_lang(lang_code)


def t(key: str) -> str:
    lang = get_lang()
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG]).get(key, key)
