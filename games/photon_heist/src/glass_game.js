function getAudioCtx() {
  if (!glassState.soundEnabled) return null;
  const AC = window.AudioContext || window.webkitAudioContext;
  if (!AC) return null;
  if (!glassAudioCtx) glassAudioCtx = new AC();
  if (glassAudioCtx.state === "suspended") glassAudioCtx.resume();
  return glassAudioCtx;
}

function playTone(freq, start = 0, duration = 0.08, vol = 0.04) {
  const ctx = getAudioCtx();
  if (!ctx) return;
  const osc = ctx.createOscillator();
  const gain = ctx.createGain();
  const t0 = ctx.currentTime + start;
  osc.type = "sine";
  osc.frequency.value = freq;
  gain.gain.setValueAtTime(vol, t0);
  gain.gain.exponentialRampToValueAtTime(0.001, t0 + duration);
  osc.connect(gain);
  gain.connect(ctx.destination);
  osc.start(t0);
  osc.stop(t0 + duration + 0.01);
}

function glassBeep(freq = 660, vol = 0.035) { playTone(freq, 0, 0.07, vol); }

function playSuccess() {
  [523, 659, 784, 1047].forEach((f, i) => playTone(f, i * 0.07, 0.18, 0.055));
}

function playAlarm() {
  playTone(220, 0, 0.12, 0.07);
  playTone(180, 0.1, 0.1, 0.06);
}

