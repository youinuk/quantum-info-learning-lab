function displaySegments() {
  if (glassState.level.prediction && !glassState.predictionCorrect) {
    const first = glassState.trace.segments[0];
    return first ? [{ ...first, planning: true }] : [];
  }
  if (glassState.level.commitMode && !glassState.committed) {
    const first = glassState.trace.segments[0];
    if (!first) return [];
    const amount = 0.24;
    return [{
      ...first,
      planning: true,
      end: {
        x: first.start.x + (first.end.x - first.start.x) * amount,
        y: first.start.y + (first.end.y - first.start.y) * amount
      }
    }];
  }
  if (!glassState.cleared) return glassState.trace.segments;
  const visible = [];
  for (const seg of glassState.trace.segments) {
    const dx = seg.end.x - seg.start.x, dy = seg.end.y - seg.start.y;
    const len2 = dx * dx + dy * dy;
    const t = len2 === 0 ? 0 : Math.max(0, Math.min(1,
      ((glassState.sensor.x - seg.start.x) * dx + (glassState.sensor.y - seg.start.y) * dy) / len2
    ));
    const near = { x: seg.start.x + dx * t, y: seg.start.y + dy * t };
    if (Math.hypot(near.x - glassState.sensor.x, near.y - glassState.sensor.y) <= glassState.sensor.radius) {
      visible.push({ ...seg, end: { x: glassState.sensor.x, y: glassState.sensor.y } });
      break;
    }
    visible.push(seg);
  }
  return visible;
}

function appendShotGhosts(svg) {
  if (!glassState.level.commitMode || !glassState.shotHistory.length) return;
  const history = glassState.committed && !glassState.cleared
    ? glassState.shotHistory.slice(0, -1)
    : glassState.shotHistory;

  history.forEach((shot, shotIndex) => {
    shot.segments.forEach(seg => {
      const start = toBoard(seg.start);
      const end = toBoard(seg.end);
      svg.appendChild(svgNode("line", {
        x1: start.x, y1: start.y, x2: end.x, y2: end.y,
        class: `shot-ghost${shot.alarmed ? " alarm-ghost" : ""}`
      }));
    });

    const finalSegment = shot.segments[shot.segments.length - 1];
    if (!finalSegment) return;
    const endpoint = toBoard(finalSegment.end);
    const marker = svgNode("g", { class: "shot-ghost-marker" });
    marker.appendChild(svgNode("circle", { cx: endpoint.x, cy: endpoint.y, r: 15 }));
    const label = svgNode("text", { x: endpoint.x, y: endpoint.y + 5 });
    label.textContent = String(shotIndex + 1);
    marker.appendChild(label);
    svg.appendChild(marker);
  });
}

