function sensorFromSolution(level) {
  let source, glass;
  if (level.rotateMode === "laser") {
    source = { ...level.source, angle: level.solutionAngle };
    glass  = { ...level.glass,  angle: level.glass.initialAngle };
  } else {
    source = level.source;
    glass  = { ...level.glass,  angle: level.solutionAngle };
  }
  const trace = Optics.traceRay({ source, glass, mirrors: level.mirrors || [] });
  const seg = trace.segments[trace.segments.length - 1];
  if (!seg) return { x: 0.9, y: 0.5, radius: 0.026 };
  const t = level.sensorT || 0.68;
  return {
    x: seg.start.x + (seg.end.x - seg.start.x) * t,
    y: seg.start.y + (seg.end.y - seg.start.y) * t,
    radius: level.sensorRadius || 0.026
  };
}

function shardFromLevel(level) {
  if (level.shardAngle === undefined) return null;
  const source = { ...level.source, angle: level.shardAngle };
  const glass = { ...level.glass, angle: level.glass.initialAngle };
  const trace = Optics.traceRay({ source, glass, mirrors: level.mirrors || [] });
  const seg = trace.segments[trace.segments.length - 1];
  if (!seg) return null;
  const t = level.shardT || 0.72;
  return {
    x: seg.start.x + (seg.end.x - seg.start.x) * t,
    y: seg.start.y + (seg.end.y - seg.start.y) * t,
    radius: level.shardRadius || 0.018
  };
}

// Danger zone
function segmentIntersectsAABB(p1, p2, rect) {
  const dx = p2.x - p1.x, dy = p2.y - p1.y;
  let tmin = 0, tmax = 1;
  for (const [p, q] of [
    [-dx, p1.x - rect.x1], [dx, rect.x2 - p1.x],
    [-dy, p1.y - rect.y1], [dy, rect.y2 - p1.y]
  ]) {
    if (Math.abs(p) < 1e-9) { if (q < 0) return false; continue; }
    const t = q / p;
    if (p < 0) tmin = Math.max(tmin, t);
    else tmax = Math.min(tmax, t);
    if (tmin > tmax) return false;
  }
  return true;
}

function evaluateDangerZones() {
  const zones = glassState.level.dangerZones;
  if (!zones || !zones.length) return false;
  return zones.some(zone =>
    glassState.trace.segments.some(seg => segmentIntersectsAABB(seg.start, seg.end, zone))
  );
}

// Persistence
function loadGlassProgress() {
  const empty = { version: 2, bestStars: {}, bestRotations: {}, bestShots: {}, challenges: {}, unlockedIndex: 0 };
  try {
    const saved = JSON.parse(localStorage.getItem("photonHeistGlassProgressV1"));
    if (!saved || typeof saved !== "object") return empty;
    const bestRotations = {};
    const bestShots = {};
    GLASS_LEVELS.forEach(level => {
      const rotations = Number(saved.bestRotations?.[level.id]);
      const shots = Number(saved.bestShots?.[level.id]);
      if (Number.isFinite(rotations) && rotations >= 0) bestRotations[level.id] = Math.floor(rotations);
      if (Number.isFinite(shots) && shots >= 1) bestShots[level.id] = Math.floor(shots);
    });
    return {
      version: 2,
      bestStars: saved.bestStars || {},
      bestRotations,
      bestShots,
      challenges: saved.challenges || {},
      unlockedIndex: Math.max(0, Math.min(GLASS_LEVELS.length - 1, Number(saved.unlockedIndex) || 0))
    };
  } catch (_) {
    return empty;
  }
}

function saveGlassProgress() {
  try { localStorage.setItem("photonHeistGlassProgressV1", JSON.stringify(glassState.progress)); }
  catch (_) {}
}

// Star and challenge logic
function calculateGlassStars() {
  if (!glassState.cleared) return 0;
  let s = 1;
  const efficientRotations = glassState.rotations <= glassState.level.targetRotations;
  const efficientShots = !glassState.level.commitMode
    || glassState.shots <= (glassState.level.targetShots || 1);
  const efficientPrediction = !glassState.level.prediction || glassState.predictionAttempts <= 1;
  if (efficientRotations && efficientShots && efficientPrediction) s += 1;
  if (glassState.hintStep === 0) s += 1;
  return s;
}

function challengeAchieved() {
  const ch = glassState.level.challenge;
  if (!glassState.cleared || !ch) return false;
  if (ch.maxRotations !== undefined && glassState.rotations > ch.maxRotations) return false;
  if (ch.maxShots !== undefined && glassState.shots > ch.maxShots) return false;
  if (ch.noHint && glassState.hintStep > 0) return false;
  if (ch.collectShard && !glassState.shardCollected) return false;
  if (ch.maxPredictions !== undefined && glassState.predictionAttempts > ch.maxPredictions) return false;
  return true;
}

function challengeDescription() {
  const ch = glassState.level.challenge;
  if (!ch) return "";
  const parts = [];
  if (ch.noHint) parts.push(glassState.lang === "ko" ? "힌트 없음" : "No hints");
  if (ch.collectShard) parts.push(gt("shardChallenge"));
  if (ch.maxPredictions !== undefined) parts.push(gt("predictionFirstTry"));
  if (ch.maxRotations !== undefined) parts.push(glassState.lang === "ko"
    ? `${ch.maxRotations}조작 이내` : `${ch.maxRotations} moves`);
  if (ch.maxShots !== undefined) parts.push(glassState.lang === "ko"
    ? `${ch.maxShots}발 이내` : `${ch.maxShots} shot(s)`);
  return parts.join(" · ");
}

function evaluateGlass() {
  glassState.trace = Optics.traceRay({
    source: currentSource(),
    glass: currentGlass(),
    mirrors: glassState.level.mirrors || []
  });
  const predictionResolved = !glassState.level.prediction || glassState.predictionCorrect;
  const resolved = predictionResolved && (!glassState.level.commitMode || glassState.committed);
  glassState.alarmed = resolved && evaluateDangerZones();
  const sensorHit = Optics.traceHitsSensor(glassState.trace, glassState.sensor);
  glassState.objectiveBlocked = Boolean(
    resolved && sensorHit && glassState.level.requiresShard && !glassState.shardCollected
  );
  glassState.cleared = resolved && sensorHit && !glassState.alarmed && !glassState.objectiveBlocked;
  glassState.stars = calculateGlassStars();
  if (glassState.cleared) {
    const id = glassState.level.id;
    glassState.progress.bestStars[id] = Math.max(glassState.progress.bestStars[id] || 0, glassState.stars);
    glassState.progress.unlockedIndex = Math.max(
      glassState.progress.unlockedIndex,
      Math.min(GLASS_LEVELS.length - 1, glassState.levelIndex + 1)
    );
    if (challengeAchieved() && !glassState.progress.challenges[id]) {
      glassState.progress.challenges[id] = true;
    }
    const previousRotations = glassState.progress.bestRotations[id];
    if (previousRotations === undefined || glassState.rotations < previousRotations) {
      glassState.progress.bestRotations[id] = glassState.rotations;
    }
    if (glassState.level.commitMode) {
      const previousShots = glassState.progress.bestShots[id];
      if (previousShots === undefined || glassState.shots < previousShots) {
        glassState.progress.bestShots[id] = glassState.shots;
      }
    }
    saveGlassProgress();
  }
}

// Level loading
function loadGlassLevel(index) {
  glassState.levelIndex = Math.max(0, Math.min(GLASS_LEVELS.length - 1, index));
  glassState.level = GLASS_LEVELS[glassState.levelIndex];
  glassState.angle = isLaserMode()
    ? glassState.level.source.angle
    : glassState.level.glass.initialAngle;
  glassState.rotations = 0;
  glassState.shots = 0;
  glassState.shotHistory = [];
  glassState.shard = shardFromLevel(glassState.level);
  glassState.shardCollected = false;
  glassState.objectiveBlocked = false;
  glassState.predictionAnswer = null;
  glassState.predictionCorrect = false;
  glassState.predictionAttempts = 0;
  glassState.committed = false;
  glassState.hintStep = 0;
  glassState.alarmed = false;
  glassState.sensor = sensorFromSolution(glassState.level);
  evaluateGlass();
  renderGlassGame();
}

// Rotation
function rotateBy(delta, stepOverride = null) {
  if (!glassState.level.rotatable) return;
  glassState.angle += delta * (stepOverride || glassState.level.stepDeg || 5);
  glassState.rotations += 1;
  glassState.committed = false;
  const wasCleared = glassState.cleared;
  const wasAlarmed = glassState.alarmed;
  evaluateGlass();
  renderGlassGame();
  if (glassState.cleared && !wasCleared) {
    playSuccess();
  } else if (glassState.alarmed && !wasAlarmed) {
    playAlarm();
  } else {
    glassBeep(delta > 0 ? 700 : 580, 0.03);
  }
}

function fireGlassLaser() {
  const budget = glassState.level.shotBudget;
  if (!glassState.level.commitMode || glassState.cleared || (budget && glassState.shots >= budget)) return;
  const wasAlarmed = glassState.alarmed;
  glassState.committed = true;
  glassState.shots += 1;
  evaluateGlass();
  if (glassState.shard && !glassState.shardCollected && !glassState.alarmed
      && Optics.traceHitsSensor(glassState.trace, glassState.shard)) {
    glassState.shardCollected = true;
  }
  if (!glassState.cleared) {
    glassState.shotHistory.push({
      angle: glassState.angle,
      alarmed: glassState.alarmed,
      segments: glassState.trace.segments.map(seg => ({
        ...seg,
        start: { ...seg.start },
        end: { ...seg.end }
      }))
    });
  }
  renderGlassGame();
  if (glassState.cleared) playSuccess();
  else if (glassState.alarmed && !wasAlarmed) playAlarm();
  else glassBeep(860, 0.08);
}

function submitPrediction(optionId) {
  const prediction = glassState.level.prediction;
  if (!prediction || glassState.cleared) return;
  glassState.predictionAnswer = optionId;
  glassState.predictionAttempts += 1;
  glassState.predictionCorrect = optionId === prediction.correct;
  evaluateGlass();
  renderGlassGame();
  if (glassState.cleared) playSuccess();
  else glassBeep(glassState.predictionCorrect ? 760 : 330, 0.08);
}

